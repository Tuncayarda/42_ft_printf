#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		i += ft_putstr("-2147483648");
	}
	else if (n < 0)
	{
		n = -n;
		i += ft_putchar('-');
		i += ft_putnbr(n);
	}
	else if (n > 9)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	else if (n <= 9)
	{
		i += ft_putchar(n + 48);
	}
	return (i);
}
